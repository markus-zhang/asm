#include "lc3disa.h"

int main()
{
	dis_br(0x0204, 	0x3010);

	// ADD 	R1, R7, R4
	dis_add(0x13e4, 0x3012);

	// Add 	R1, R7, #0x11c
	dis_add(0x13fc, 0x3014);


	dis_ld(0x2c17, 	0x3016);
	dis_st(0x3cff, 	0x3918);
	dis_jsr(0x49fe, 0x301a);
	dis_jsr(0x41c0, 0x301c);

	// AND	R1, R7, R4
	dis_and(0x53e4, 0x301e);

	// AND 	R1, R7, #0x11c
	dis_and(0x53fc, 0x3020);

	// LDR	R2, R4, #0x0f
	dis_ldr(0x650f, 0x3022);

	// STR	R4, R2, #5
	dis_str(0x7885, 0x3024);

	// RTI
	dis_rti(0x8000, 0x3026);

	// NOT	R0, R7
	dis_not(0x91ff, 0x3028);

	// LDI	R4, #0x01a7
	dis_ldi(0xa9a7, 0x302a);

	// STI	R6, #0x0055
	dis_sti(0xbc55, 0x302c);

	// RET
	dis_jmp(0xc1c0, 0x302e);

	// JMP 	R5
	dis_jmp(0xc140, 0x3030);

	// Reserved
	dis_rsv(0xd000, 0x3032);

	// LEA	R3, 0x0044
	dis_lea(0xe644, 0x3034);

	// TRAP
	dis_trap(0xf020, 0x3036);
	dis_trap(0xf021, 0x3038);
	dis_trap(0xf022, 0x303a);
	dis_trap(0xf023, 0x303c);
	dis_trap(0xf024, 0x303e);
	dis_trap(0xf025, 0x3040);
	dis_trap(0xf026, 0x3042);

	return 0;
}